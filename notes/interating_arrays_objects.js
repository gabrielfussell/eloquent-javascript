//solution from book to create a table
function tableFor(journal, event) {
    let table = [0, 0, 0, 0];
    for(let i = 0; i < journal.length; ++i) {
        let entry = journal[i], index = 0;
        if(entry.events.includes(event)) index += 1;
        if(entry.squirrel) index += 2;
        table[index] += 1;
    }
    return table;
}

//my solution to create a table
function createTable(journal, searchEvent) {
    let table = [0, 0, 0, 0];

    for(journalEntry of journal) {
        let squirrel = journalEntry.squirrel;
        let includesSearchEvent = journalEntry.events.includes(searchEvent);
      
        if(squirrel == false && includesSearchEvent == false) ++table[0];
        else if(squirrel == false && includesSearchEvent == true) ++table[1];
        else if(squirrel == true && includesSearchEvent == false) ++table[2];
        else if(squirrel == true && includesSearchEvent == true) ++table[3];
    }

    return table;
}

/*
Phi function is for correlation.
Returns a number between -1 and 1
1 is perfectly correlated
-1 is always opposite
0 is no correlation

Array indexes and their binary equivalent:
0, 1, 2, 3
00, 01, 10, 11
*/
function phi(table) {
    return (
        (table[3] * table[0] - table[2] * table[1])
        / Math.sqrt(
            (table[2] + table[3])
            * (table[0] + table[1])
            * (table[1] + table[3])
            * (table[0] + table[2])
            )
    );
}

//produce an array of all unique journal events
function journalEvents(journal) {
    let allEvents = [];

    for(let entry of journal) {
        for(let event of entry.events) {
            allEvents.push(event);
        }
    }

    let uniqueEvents = [...new Set(allEvents)];
    return uniqueEvents;
}


